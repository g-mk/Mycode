    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String staffName = txtStaffName.getText();
        String staffId = txtStaffID.getText();
        String salary = txtSalary.getText();
        if(!staffId.equals("") && !staffName.equals("") && !salary.equals(""))
        {
            try {
                con = DbConnect.Connect();
                String query = "INSERT INTO staff(staffid,staffname,staffsalary,stafftype) "
                + "VALUES(?,?,?,?)";
                PreparedStatement stmt = con.prepareStatement(query);
                stmt.setString(1, staffId);
                stmt.setString(2, staffName);
                stmt.setDouble(3,Double.parseDouble(salary));
                stmt.setString(4, cmbxStaffType.getSelectedItem().toString());
                int stat = stmt.executeUpdate();
                if(stat>0)
                {
                    JOptionPane.showMessageDialog(rootPane, "Staff Added to the List", "Staff Enrollment", JOptionPane.INFORMATION_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Staff Not Added to the List", "Staff Enrollment", JOptionPane.ERROR_MESSAGE);
                }
                con.close();
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Staff Not Added to the List "+ex.getMessage(), "Staff Enrollment", JOptionPane.ERROR_MESSAGE);
            }

        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Enter All the Feilds", "Staff Enroll", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

