    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mid = txtMedicineID.getText();
        String mname = txtMedicineName.getText();
        String mprice = txtMedicinePrice.getText();
        String mqty = txtMedicineQty.getText();
        String mDesc = txtMedicineDesc.getText();
        if(mid!=null && mname!=null && mprice!=null && mqty!=null && mDesc!=null){
            int qty = Integer.parseInt(mqty);
            int price = Integer.parseInt(mprice);
            if(qty>0)
            {
                try {
                    con = DbConnect.Connect();
                    String sql = "INSERT INTO medicine VALUES(?,?,?,?,?,?,?)";
                    PreparedStatement stmt = con.prepareStatement(sql);
                    stmt.setString(1, mid);
                    stmt.setString(2, mname);
                    stmt.setFloat(3, price);
                    String mgf = cmbxMgfYear.getSelectedItem().toString()+"-";
                    mgf+=cmbxMgfMonth.getSelectedItem().toString()+"-";
                    mgf+=cmbxMgfDate.getSelectedItem().toString();
                    String exp = cmbxExpYear.getSelectedItem().toString()+"-";
                    exp+=cmbxExpMonth.getSelectedItem().toString()+"-";
                    exp+=cmbxExpDate.getSelectedItem().toString();
                    stmt.setString(4, mgf);
                    stmt.setString(5, exp);
                    stmt.setInt(6, qty);
                    stmt.setString(7, mDesc);
                    int stat = stmt.executeUpdate();
                    if(stat>0)
                    {
                        JOptionPane.showMessageDialog(rootPane, "Inventory Added to the List", "Inventory", JOptionPane.INFORMATION_MESSAGE);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane, "Inventory Not Added to the List", "Inventory", JOptionPane.ERROR_MESSAGE);
                    }

                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                finally{
                    try {
                        con.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            else
            JOptionPane.showMessageDialog(rootPane, "Provide a Valid Quantity", "Medical Inventory", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        JOptionPane.showMessageDialog(rootPane, "Provide All the Details", "Medical Inventory", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

