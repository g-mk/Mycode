    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String Mid = cmbxUpdateMid.getSelectedItem().toString();
        if(!Mid.equals("--Select--"))
        {
            Medicine m = new Medicine(Mid);
            try {
                m.getMedicineDetails();
                String mgf = txtMgfDate.getText();
                String exp = txtExpDate.getText();
                String [] mgfToken = mgf.split("[-]");
                String [] expToken = exp.split("[-]");
                
                Date mDate = new Date(Integer.parseInt(mgfToken[0])-1900,Integer.parseInt(mgfToken[1])-1,Integer.parseInt(mgfToken[2]));
                Date eDate = new Date(Integer.parseInt(expToken[0])-1900,Integer.parseInt(expToken[1])-1,Integer.parseInt(expToken[2]));
                m.setName(txtMedicineName1.getText());
                m.setDesc(txtMedicineDesc1.getText());
                m.setQuantity(Integer.parseInt(txtMedicineQty1.getText()));
                m.setPrice(Float.parseFloat(txtMedicinePrice1.getText()));
                m.setMgfDate(mDate);
                m.setExpDate(eDate);
                if(m.updateAll())
                    JOptionPane.showMessageDialog(rootPane, "Inventory Updated in the List", "Update Inventory", JOptionPane.INFORMATION_MESSAGE);
                else
                    JOptionPane.showMessageDialog(rootPane, "Inventory Not Updated in the List", "Update Inventory", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

